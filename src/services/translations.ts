export function t(key: string, vars?: Record<string, string | number>, lang?: Language): string {
  const l = lang || 'en'
  let text = translations[l]?.[key] || translations.en[key] || key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v))
    }
  }
  return text
}

